def get_config(key: str, default: str = None) -> str:
    try:
        row = spark.sql(f"""
            SELECT config_value FROM {PREFIX}.harvest_config WHERE config_key = '{key}'
        """).first()
        return row.config_value if row else default
    except Exception:
        return default

FAILURE_THRESHOLD = float(get_config("alert_failure_rate_threshold", "0.15"))
COST_SPIKE_THRESHOLD = float(get_config("alert_cost_spike_threshold", "2.0"))
LATENCY_P95_THRESHOLD = int(get_config("alert_latency_p95_threshold_ms", "30000"))
RETENTION_DAYS = int(get_config("retention_days", "90"))

print(f"Thresholds: failure>{FAILURE_THRESHOLD*100}%, cost spike>{COST_SPIKE_THRESHOLD}x, "
      f"latency p95>{LATENCY_P95_THRESHOLD}ms, retention={RETENTION_DAYS}d")

