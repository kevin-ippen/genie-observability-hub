# MAGIC ## FAQ Detection (most common questions by space)

# COMMAND ----------

spark.sql(f"""
CREATE OR REPLACE TABLE {PREFIX}.faq_patterns AS
WITH normalized_queries AS (
  SELECT
    space_id,
    workspace_id,
    LOWER(TRIM(user_query)) AS query_normalized,
    user_query AS sample_query,
    COUNT(*) AS frequency,
    COUNT(DISTINCT user_email) AS unique_users,
    AVG(CASE WHEN status = 'COMPLETED' THEN 1.0 ELSE 0.0 END) AS success_rate,
    AVG(duration_ms) AS avg_duration_ms
  FROM {PREFIX}.genie_messages
  WHERE role = 'USER'
    AND user_query IS NOT NULL
    AND LENGTH(user_query) > 5
    AND created_at >= current_date() - INTERVAL 30 DAYS
  GROUP BY space_id, workspace_id, LOWER(TRIM(user_query)), user_query
)
SELECT
  space_id,
  workspace_id,
  sample_query,
  frequency,
  unique_users,
  ROUND(success_rate, 3) AS success_rate,
  ROUND(avg_duration_ms, 0) AS avg_duration_ms,
  ROW_NUMBER() OVER (PARTITION BY space_id ORDER BY frequency DESC) AS rank_in_space
FROM normalized_queries
WHERE frequency >= 2
QUALIFY rank_in_space <= 50
""")

faq_count = spark.sql(f"SELECT COUNT(*) FROM {PREFIX}.faq_patterns").first()[0]
print(f"✓ FAQ patterns: {faq_count} entries across all spaces")

