# MAGIC ## Monitor: Latency Degradation (p95 > threshold)

# COMMAND ----------

# DBTITLE 1,Cell 12
spark.sql(f"""
INSERT INTO {PREFIX}.alert_events (alert_id, alert_type, severity, space_id, workspace_id,
                                    metric_value, threshold_value, message, context, created_at)
SELECT
  uuid(),
  'latency_degradation',
  'warning',
  space_id,
  workspace_id,
  ROUND(p95_ms, 0),
  {LATENCY_P95_THRESHOLD},
  CONCAT('p95 latency = ', ROUND(p95_ms/1000, 1), 's (threshold: ', {LATENCY_P95_THRESHOLD}/1000, 's)'),
  NULL,
  current_timestamp()
FROM (
  SELECT space_id, workspace_id,
    PERCENTILE(duration_ms, 0.95) AS p95_ms
  FROM {PREFIX}.genie_messages
  WHERE created_at >= current_timestamp() - INTERVAL 24 HOURS
    AND duration_ms IS NOT NULL
    AND role = 'ASSISTANT'
  GROUP BY space_id, workspace_id
  HAVING COUNT(*) >= 10
)
WHERE p95_ms > {LATENCY_P95_THRESHOLD}
  AND NOT EXISTS (
    SELECT 1 FROM {PREFIX}.alert_events ae
    WHERE ae.space_id = space_id
      AND ae.alert_type = 'latency_degradation'
      AND ae.created_at >= current_timestamp() - INTERVAL 24 HOURS
  )
""")

