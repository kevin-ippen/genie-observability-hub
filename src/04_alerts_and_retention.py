# MAGIC ## Monitor: Cost Spike (daily DBUs > 2x 7-day average)

# COMMAND ----------

# DBTITLE 1,Cell 10
spark.sql(f"""
INSERT INTO {PREFIX}.alert_events (alert_id, alert_type, severity, space_id, workspace_id,
                                    metric_value, threshold_value, message, context, created_at)
WITH today_cost AS (
  SELECT workspace_id, user_email,
    SUM(total_dbus) AS today_dbus
  FROM {PREFIX}.genie_cost_attribution
  WHERE usage_date = current_date() - 1  -- yesterday (most recent complete day)
  GROUP BY workspace_id, user_email
),
avg_cost AS (
  SELECT workspace_id, user_email,
    AVG(daily_total) AS avg_daily_dbus
  FROM (
    SELECT workspace_id, user_email, usage_date, SUM(total_dbus) AS daily_total
    FROM {PREFIX}.genie_cost_attribution
    WHERE usage_date BETWEEN current_date() - 8 AND current_date() - 2
    GROUP BY workspace_id, user_email, usage_date
  )
  GROUP BY workspace_id, user_email
  HAVING AVG(daily_total) > 0
)
SELECT
  uuid(),
  'cost_spike',
  CASE WHEN t.today_dbus / a.avg_daily_dbus > 5.0 THEN 'critical' ELSE 'warning' END,
  NULL,
  t.workspace_id,
  ROUND(t.today_dbus, 2),
  ROUND(a.avg_daily_dbus * {COST_SPIKE_THRESHOLD}, 2),
  CONCAT(t.user_email, ': ', ROUND(t.today_dbus, 1), ' DBUs yesterday vs ',
         ROUND(a.avg_daily_dbus, 1), ' avg (',
         ROUND(t.today_dbus / a.avg_daily_dbus, 1), 'x spike)'),
  NULL,
  current_timestamp()
FROM today_cost t
JOIN avg_cost a ON t.workspace_id = a.workspace_id AND t.user_email = a.user_email
WHERE t.today_dbus > a.avg_daily_dbus * {COST_SPIKE_THRESHOLD}
  AND NOT EXISTS (
    SELECT 1 FROM {PREFIX}.alert_events ae
    WHERE ae.alert_type = 'cost_spike'
      AND ae.message LIKE CONCAT(t.user_email, '%')
      AND ae.created_at >= current_timestamp() - INTERVAL 24 HOURS
  )
""")

