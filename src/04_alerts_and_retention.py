# MAGIC %md
# MAGIC ## Monitor: Failure Rate Spike (per space, last 24h vs prior 7d)

# COMMAND ----------

# DBTITLE 1,Cell 8
spark.sql(f"""
INSERT INTO {PREFIX}.alert_events (alert_id, alert_type, severity, space_id, workspace_id,
                                    metric_value, threshold_value, message, context, created_at)
WITH recent AS (
  SELECT space_id, workspace_id,
    COUNT(*) AS total,
    SUM(CASE WHEN status = 'FAILED' THEN 1 ELSE 0 END) AS failed
  FROM {PREFIX}.genie_messages
  WHERE created_at >= current_timestamp() - INTERVAL 24 HOURS
    AND role = 'ASSISTANT'
  GROUP BY space_id, workspace_id
  HAVING total >= 5  -- minimum sample
),
baseline AS (
  SELECT space_id,
    SUM(CASE WHEN status = 'FAILED' THEN 1 ELSE 0 END) * 1.0 / COUNT(*) AS baseline_rate
  FROM {PREFIX}.genie_messages
  WHERE created_at BETWEEN current_timestamp() - INTERVAL 8 DAYS
                       AND current_timestamp() - INTERVAL 1 DAY
    AND role = 'ASSISTANT'
  GROUP BY space_id
  HAVING COUNT(*) >= 10
)
SELECT
  uuid(),
  'failure_rate_spike',
  CASE WHEN r.failed * 1.0 / r.total > 0.5 THEN 'critical' ELSE 'warning' END,
  r.space_id,
  r.workspace_id,
  ROUND(r.failed * 1.0 / r.total, 3),
  {FAILURE_THRESHOLD},
  CONCAT('Failure rate ', ROUND(r.failed * 100.0 / r.total, 1), '% in last 24h (', r.failed, '/', r.total, ' messages)'),
  NULL,
  current_timestamp()
FROM recent r
LEFT JOIN baseline b ON r.space_id = b.space_id
WHERE r.failed * 1.0 / r.total > {FAILURE_THRESHOLD}
  AND r.failed * 1.0 / r.total > COALESCE(b.baseline_rate * 1.5, {FAILURE_THRESHOLD})
  -- Deduplicate: don't re-alert if same space alerted in last 24h
  AND NOT EXISTS (
    SELECT 1 FROM {PREFIX}.alert_events ae
    WHERE ae.space_id = r.space_id
      AND ae.alert_type = 'failure_rate_spike'
      AND ae.created_at >= current_timestamp() - INTERVAL 24 HOURS
  )
""")

