# MAGIC ## Data Retention: Purge old records

# COMMAND ----------

tables_to_purge = ["genie_messages", "genie_conversations", "genie_query_executions"]

for table in tables_to_purge:
    fqn = f"{PREFIX}.{table}"
    try:
        result = spark.sql(f"""
            DELETE FROM {fqn}
            WHERE harvested_at < current_date() - INTERVAL {RETENTION_DAYS} DAYS
        """)
        deleted = result.first()[0] if result.first() else 0
        if deleted:
            print(f"  🗑️ {table}: purged {deleted} rows older than {RETENTION_DAYS} days")
        else:
            print(f"  ✓ {table}: nothing to purge")
    except Exception as e:
        print(f"  ⚠️ {table}: {e}")

# Vacuum to reclaim storage
for table in tables_to_purge:
    try:
        spark.sql(f"OPTIMIZE {PREFIX}.{table}")
    except Exception:
        pass

