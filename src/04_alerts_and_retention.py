# MAGIC ## SLA Metrics (per space, per day)

# COMMAND ----------

spark.sql(f"""
CREATE OR REPLACE TABLE {PREFIX}.sla_metrics AS
SELECT
  space_id,
  workspace_id,
  DATE(created_at) AS metric_date,
  COUNT(*) AS total_messages,
  SUM(CASE WHEN status = 'COMPLETED' THEN 1 ELSE 0 END) AS completed,
  SUM(CASE WHEN status = 'FAILED' THEN 1 ELSE 0 END) AS failed,
  ROUND(SUM(CASE WHEN status = 'COMPLETED' THEN 1 ELSE 0 END) * 100.0 / COUNT(*), 1) AS success_rate_pct,
  ROUND(AVG(duration_ms), 0) AS avg_duration_ms,
  ROUND(PERCENTILE(duration_ms, 0.5), 0) AS p50_duration_ms,
  ROUND(PERCENTILE(duration_ms, 0.95), 0) AS p95_duration_ms,
  ROUND(PERCENTILE(duration_ms, 0.99), 0) AS p99_duration_ms,
  COUNT(DISTINCT user_email) AS unique_users,
  SUM(CASE WHEN feedback_rating = 'POSITIVE' THEN 1 ELSE 0 END) AS positive_feedback,
  SUM(CASE WHEN feedback_rating = 'NEGATIVE' THEN 1 ELSE 0 END) AS negative_feedback
FROM {PREFIX}.genie_messages
WHERE role = 'ASSISTANT'
  AND created_at >= current_date() - INTERVAL 90 DAYS
GROUP BY space_id, workspace_id, DATE(created_at)
""")

sla_days = spark.sql(f"SELECT COUNT(DISTINCT metric_date) FROM {PREFIX}.sla_metrics").first()[0]
print(f"✓ SLA metrics: {sla_days} days of data")

